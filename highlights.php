                                pee-walks.</li>
                            <li>They can be picky eaters and have temperamental intestines.</li>
                            <li>Socialization is a lifelong task.</li>
                            <li> Giants get bored very quickly and not capable of self-entertainment.</li>
                            <li> You will need to learn how to groom your own dog. Most groomers lack the capabilities
